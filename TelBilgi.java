package Entity;


public class TelBilgi {
	
	int telefonID;
	String telTip;
	String numara;
	
	public int getTelefonID() {
		return telefonID;
	}
	public void setTelefonID(int telefonID) {
		this.telefonID = telefonID;
	}
	
	public String getTelTip() {
		return telTip;
	}
	public void setTelTip(String telTip) {
		this.telTip = telTip;
	}
	
	public String getNumara() {
		return this.numara;
	}
	
	public void setNumara(String numara) {
		this.numara=numara;
	}
}
